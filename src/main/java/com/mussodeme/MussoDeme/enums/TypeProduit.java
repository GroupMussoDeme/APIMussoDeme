package com.mussodeme.MussoDeme.enums;

/**
 * Types de produits pour la recherche vocale
 * Permet aux femmes rurales de chercher des produits par catégorie via audio
 */
public enum TypeProduit {
    /**
     * Produits alimentaires (céréales, légumes, fruits, etc.)
     */
    ALIMENTAIRE,
    
    /**
     * Produits artisanaux (savons, beurre de karité, bijoux, etc.)
     */
    ARTISANAT,
    
    /**
     * Produits textiles (pagnes, vêtements, tissus, etc.)
     */
    TEXTILE,
    
    /**
     * Produits cosmétiques naturels (huiles, crèmes, etc.)
     */
    COSMETIQUE,
    
    /**
     * Produits agricoles (semences, outils, engrais naturels, etc.)
     */
    AGRICOLE,
    
    /**
     * Produits ménagers (paniers, nattes, ustensiles, etc.)
     */
    MENAGER,
    
    /**
     * Autres produits
     */
    AUTRE
}
